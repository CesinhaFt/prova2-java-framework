    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        if (nomeTxt.getText().trim().isEmpty() || strCPF.getText().trim().isEmpty() || intEsp.getValue() == null || intCRM.getValue() == null) {
            JOptionPane.showMessageDialog(rootPane, "PREENCHA TODOS OS CAMPOS");
        } else {
            Medico medico = new Medico();
            medico.setEspecialidade((Integer) intEsp.getValue());
            medico.setCRM((Integer) intCRM.getValue());
            medico.setNome(nomeTxt.getText().trim());
            medico.setCPF(strCPF.getText().trim());
            String id = idTxt.getText().trim();
            String msg;
            if (id.isEmpty()) {
                msg = dao.cadastrarMedico(medico);
                if (msg.equals("1")) {
                    msg = "CADASTRO EFETUADO COM SUCESSO";
                }
                JOptionPane.showMessageDialog(rootPane, msg);
            } else {
                medico.setId(Integer.parseInt(id));
                msg = dao.atualizarMedico(medico);
                if (msg.equals("1")) {
                    msg = "ALTERAÇÃO EFETUADA COM SUCESSO";
                }
                JOptionPane.showMessageDialog(rootPane, msg);
            }
            
            this.limparCampos();
            this.preencherTabela();
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void medicosTblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_medicosTblMouseClicked
        // TODO add your handling code here:
        btnExcluir.setEnabled(true);
        idTxt.setText(medicosTbl.getValueAt(medicosTbl.getSelectedRow(), 0).toString());
        nomeTxt.setText(medicosTbl.getValueAt(medicosTbl.getSelectedRow(), 1).toString());
        intEsp.setValue(medicosTbl.getValueAt(medicosTbl.getSelectedRow(), 2));
        intCRM.setValue(medicosTbl.getValueAt(medicosTbl.getSelectedRow(), 3));
        strCPF.setText(medicosTbl.getValueAt(medicosTbl.getSelectedRow(), 4).toString());
    }//GEN-LAST:event_medicosTblMouseClicked

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        int resposta = JOptionPane.showConfirmDialog(rootPane, "DESEJA EXCLUIR O REGISTRO?");
        if (resposta == 0) {
            int id = Integer.parseInt(idTxt.getText());
            String msg = dao.excluirMedico(id);
            if (msg.equals("1")) {
                msg = "EXCLUSÃO EFETUADA COM SUCESSO";
            }
            JOptionPane.showMessageDialog(rootPane, msg);
            
            this.limparCampos();
            this.preencherTabela();
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

