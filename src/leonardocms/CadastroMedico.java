    public void preencherTabela() {
        ArrayList medicos;
        DefaultTableModel tabela = (DefaultTableModel) medicosTbl.getModel();
        tabela.setRowCount(0);
        medicos = dao.listarMedico();
        Iterator it = medicos.iterator();
        while (it.hasNext()) {
            Medico med = (Medico) it.next();
            tabela.addRow(new Object[]{med.getId(), med.getNome(), med.getEspecialidade(), med.getCRM(), med.getCPF()});
        }
    }

